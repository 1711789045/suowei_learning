#include "pid.h"

// 全局PID参数(左右电机共享,可通过菜单调整)
float motor_kp = 0.0f;          // 比例系数(初值=0)
float motor_ki = 0.0f;          // 积分系数(初值=0)
float motor_kd = 0.0f;          // 微分系数(初值=0)

/*********************************************************************************************************************
* 函数名称: pid_init
* 功能说明: 初始化PID参数
* 参数说明: 无
* 返回值:   无
* 备注:     参数初始值为0,需通过菜单调整
********************************************************************************************************************/
void pid_init(void)
{
    motor_kp = 0.0f;
    motor_ki = 0.0f;
    motor_kd = 0.0f;

    printf("[PID] Init OK - Kp=%.2f Ki=%.2f Kd=%.2f\r\n", motor_kp, motor_ki, motor_kd);
}

/*********************************************************************************************************************
* 函数名称: pid_reset
* 功能说明: 重置PID状态(清空历史误差)
* 参数说明: pid - PID状态结构体指针
* 返回值:   无
* 备注:     切换控制目标或启动时调用,避免历史误差影响
********************************************************************************************************************/
void pid_reset(pid_t *pid)
{
    pid->error_last = 0.0f;
    pid->error_sum = 0.0f;
}

/*********************************************************************************************************************
* 函数名称: pid_calc
* 功能说明: 计算PID输出(增量式)
* 参数说明: pid    - PID状态结构体指针
*           target - 目标值
*           actual - 实际值
* 返回值:   float - PID控制输出
* 备注:     增量式PID公式: output = Kp×e + Ki×Σe + Kd×Δe
********************************************************************************************************************/
float pid_calc(pid_t *pid, float target, float actual)
{
    // 计算当前误差
    float error = target - actual;

    // 更新误差累积(积分项)
    pid->error_sum += error;

    // 计算误差变化(微分项)
    float error_delta = error - pid->error_last;

    // 增量式PID计算
    float output = motor_kp * error           // 比例项
                 + motor_ki * pid->error_sum  // 积分项
                 + motor_kd * error_delta;    // 微分项

    // 保存当前误差供下次使用
    pid->error_last = error;

    return output;
}
